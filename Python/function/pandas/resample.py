import pandas as pd
import numpy as np

n = 10
df = pd.DataFrame({"A": [ i for i in range(n) ]}, 
                  index = pd.date_range("2022-08-25", periods = n, freq = 'S'))
for i in [1, 2, 3]:
    df.iloc[i] = np.nan
print(df)
'''
                       A
2022-08-25 00:00:00  0.0
2022-08-25 00:00:01  NaN
2022-08-25 00:00:02  NaN
2022-08-25 00:00:03  NaN
2022-08-25 00:00:04  4.0
2022-08-25 00:00:05  5.0
2022-08-25 00:00:06  6.0
2022-08-25 00:00:07  7.0
2022-08-25 00:00:08  8.0
2022-08-25 00:00:09  9.0
'''

print('---------------------------------------------------------------------')
sample_rate = 3
df = df.resample(f'{sample_rate}S', offset = '1S').mean() # We may still suffer from NaN values
print(df)
'''
                       A
2022-08-24 23:59:58  0.0
2022-08-25 00:00:01  NaN
2022-08-25 00:00:04  5.0
2022-08-25 00:00:07  8.0
'''

print('---------------------------------------------------------------------')
new_df = df.interpolate()
print(new_df)
'''
                       A
2022-08-24 23:59:58  0.0
2022-08-25 00:00:01  2.5
2022-08-25 00:00:04  5.0
2022-08-25 00:00:07  8.0
'''

print('---------------------------------------------------------------------')
new_df = df.fillna(0)
print(new_df)
'''
                       A
2022-08-24 23:59:58  0.0
2022-08-25 00:00:01  2.5
2022-08-25 00:00:04  5.0
2022-08-25 00:00:07  8.0
'''